const fs = require('fs');
const path = require('path');

const DEFAULT_API_BASE_URL = 'http://3.35.218.85:3000';
const DEFAULT_TIMEOUT_MS = 30000;

function normalizeBaseUrl(url) {
    return url.replace(/\/+$/, '');
}

function parseTimeout(value) {
    const timeout = Number.parseInt(value, 10);

    if (Number.isFinite(timeout) && timeout > 0) {
        return timeout;
    }

    return DEFAULT_TIMEOUT_MS;
}

const apiBaseUrl = normalizeBaseUrl(
    process.env.API_BASE_URL || DEFAULT_API_BASE_URL,
);
const timeoutMs = parseTimeout(process.env.DEFAULT_TIMEOUT_MS);
const outputPath = path.join(
    __dirname,
    '..',
    'src',
    'api',
    'http',
    'runtimeConfig.js',
);

const fileContents = `// This file is autogenerated by scripts/generate-runtime-config.js
// Do not edit directly.

export const API_BASE_URL = '${apiBaseUrl}';
export const DEFAULT_TIMEOUT_MS = ${timeoutMs};
`;

fs.writeFileSync(outputPath, fileContents);

console.log(
    `[generate-runtime-config] API_BASE_URL=${apiBaseUrl} DEFAULT_TIMEOUT_MS=${timeoutMs}`,
);
